{
    for(var i=0; i<total_num; i++)
    {
        //二级菜单
        var el = document.getElementById('menu_sub_'+i);
 
        if(!el) return;
 
        //一级菜单前面的图片
        var img = document.getElementById('menu_master_'+i);
 
        if(i == curr_id)
        {
            el.style.display = "block";
           <!-- img.src = "img/open.gif"; -->
        }
        else
        {
            el.style.display = "none";
            <!-- img.src = "img/close.gif"; -->
        }
    }
}
